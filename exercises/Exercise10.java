package com.jordanamcr.exercises;

public class Exercise10 {

    public static void main(String[] args) {
        int[] integers = new int[10];
        for (int i = 0; i < 10; i++) {
            integers[i] = i + 1;
            System.out.print(integers[i]);
            System.out.print(" ");
        }
        System.out.println("");
        System.out.println("---------------------------------");
    }

}
